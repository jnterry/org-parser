"use strict";

global.expect = require('chai').expect;

/////////////////////////////////////////////////////////////////////
/// \brief Helper function which imports a file containing a test suite
/////////////////////////////////////////////////////////////////////
function importTest(name, path){
	if(path == null){ path = name; }

	describe(name, function(){
		require("./" + path);
	});
}

describe('org-parser', () => {
	importTest('test');
});
