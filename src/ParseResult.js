"use strict";

let TextLocation = require('./TextLocation');

////////////////////////////////////////////////////////////////
/// ParseError
////////////////////////////////////////////////////////////////
let ParseError = function(start, end, message){
	this.loc     = {
		start : start,
		end   : end,
	};
	this.message = message;
};


////////////////////////////////////////////////////////////////
/// ParseResult Below
////////////////////////////////////////////////////////////////
let ParseResult = function(){
	this.value  = null;
	this.errors = [];
};

ParseResult.Error = ParseError;

ParseResult.prototype.appendError = function(error){
	this.errors.push(error);
};




module.exports = ParseResult;
